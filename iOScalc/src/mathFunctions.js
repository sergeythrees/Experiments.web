/**@param {number} first*/
/**@param {number} second*/
function divide(first, second) {
  return first / second;
}

/**@param {number} first*/
/**@param {number} second*/
function multiple(first, second) {
  return first * second;
}

/**@param {number} first*/
/**@param {number} second*/
function sum(first, second) {
  return first + second;
}

/**@param {number} first*/
/**@param {number} second*/
function subtract(first, second) {
  return first - second;
}